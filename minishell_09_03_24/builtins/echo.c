#include "./builtins.h"
//extern	int	g_exit_status;

static	int	nb_args(char **args)
{
	int		size;

	size = 0;
	while (args[size])
		size++;
	return (size);
}

int	valid_n_option(char *argv)
{
	int	i;

	i = 1;
	if (argv[0] == '-')
	{
		while (argv[i] == 'n')
			i++;
	}
	if (!argv[i])
		return (1);
	else
		return (0);
}

int	ft_echo(char **args)
{
	int		i;
	int		n_option;

	i = 1;
	n_option = 0;
	if (nb_args(args) > 1)
	{
		while (args[i] && valid_n_option (args[i]))
		{
			n_option = 1;
			i++;
		}
		while (args[i])
		{
			ft_putstr_fd(args[i], 1);
			if (args[i + 1] && args[i][0] != '\0')
				write(1, " ", 1);
			i++;
		}
	}
	if (n_option == 0)
		write(1, "\n", 1);
	return (0);
}
